let bilangan = 5;

// Operator perbanding digunakan untuk membandingkan dua data atau nilai 

//Sama dengan == (Cek nilai)
console.log(`Hasil dari cek bilangan sama dengan ${bilangan} == "5" adalah ${bilangan == "5"}`)

//Sama dengan === (Cek nilai dan tipe data)
console.log(`Hasil dari cek bilangan sama dengan ${bilangan} === "5" adalah ${bilangan === "5"}`)

//Tidak sama dengan != (Cek nilai)
console.log(`Hasil dari cek tidak sama dengan ${bilangan} != "5" adalah ${bilangan != "5"}`)

//Tidak sama dengan !== (Cek nilai dan tipe data)
console.log(`Hasil dari cek tidak sama dengan ${bilangan} !== 4 adalah ${bilangan !== 4}`)

//Lebih dari >
console.log(`Hasil dari cek lebih dari ${bilangan} > 2 adalah ${bilangan > 2}`)

//Kurang dari <
console.log(`Hasil dari cek kurang dari ${bilangan} < 10 ${bilangan < 10}`)

//Lebih dari atau sama dengan >=
console.log(`Hasil dari cek lebih dari atau sama dengan ${bilangan} >= "5" adalah ${bilangan >= "5"}`)

//Kurang dari atau sama dengan <=
console.log(`Hasil dari cek kurang dari atau sama dengan ${bilangan} <= 5 adalah ${bilangan <= 5}`)



//Penjumlahan (+)
let num1 = 5;
let num2 = 3;
console.log("Hasil Penjumlahan:", num1 + num2)

//Pengurangan (-)
console.log("Hasil Pengurangan:", num1 - num2)

//Pembagian (/)
console.log("Hasil Pembagian:", num1 / num2)

//Perkalian (*)
console.log("Hasil perkalian:", num1 * num2)

//Pangkat (**)
console.log("Hasil Pangkat:", num1 ** num2)

//Modulus (%) Menghasilkan sisa bagi
console.log("Hasil modulus:", num1 % num2)

//Increment (++) menambah 1 
num1++;
console.log("Hasil increment:", num1)

//Decrement (--) mengurangi 1
num1--;
console.log("Hasil decrement:", num1)
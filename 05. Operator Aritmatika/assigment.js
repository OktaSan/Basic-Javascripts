let num1 = 5;
let num2 = 3;

//Addiction Assignment (+=)
num1 += 5;
console.log("Hasil addiction assignment:", num1)

//Subtraction Addigment (-=)
num1 -= 6;
console.log("Hasil subtraction assignment:", num1)

//Multiplication Assignment (*=)
num2 *= 9;
console.log("Hasil multiplication assignment:", num2)

//Division Assignment (/=)
num2 /= 3
console.log("Hasil Division assignment:", num2)

//Modulus Assignment (%=)
num2 %= 5;
console.log("Hasil Modulus assignment:", num2)

//Exponen Assignment (**=)
num2 **= 2;

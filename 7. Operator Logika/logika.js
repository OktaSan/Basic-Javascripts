//Operator logika digunakan untuk menentukan logika antara dua nilai atau kondisi
let bool1 = true;
let bool2 = false;

//AND (&&)
//Kedua nilai harus True agar mendapatkan hasil True
console.log(bool1 && bool2)

//OR (||)
//Salah satu nilai True maka akan hasilnya akan True
console.log(bool1 || bool2)

//NOT (!)
//Kebalikkannya
console.log(!bool1)
// Terdapat 2 variabel yang berbeda di bahasa Javascripts ini, antara lain:

// (let) variabel yang bisa kita ubah nilainya kapan saja 
let umur = 25;
umur = 26;

console.log(umur);

// (const) Variabel yang tidak bisa diubah nilainya saat sudah di tetapkan di awal
// Jika memaksa merubahnya akan terjadi error 
const nama = "Agus";

console.log(nama)